#!/usr/bin/env node

import { readdir, readFile, stat, writeFile } from "node:fs/promises";
import { dirname, join, relative, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const sampleDirectory = dirname(fileURLToPath(import.meta.url));
const templatesDirectory = resolve(sampleDirectory, "..");
const outputFile = join(sampleDirectory, "template-projects.js");

async function isFile(path) {
    try {
        return (await stat(path)).isFile();
    } catch {
        return false;
    }
}

const entries = await readdir(templatesDirectory, { withFileTypes: true });
const projects = [];

for (const entry of entries) {
    if (!entry.isDirectory() || entry.name.startsWith(".") || entry.name === "template-sample") continue;

    const directory = join(templatesDirectory, entry.name);
    const jsonPath = join(directory, "template.json");
    if (!(await isFile(jsonPath))) continue;

    try {
        const json = await readFile(jsonPath, "utf8");
        const template = JSON.parse(json);
        const htmlFile = template.html || "index.html";
        const cssFile = template.css || "style.css";
        const htmlPath = join(directory, htmlFile);
        const cssPath = join(directory, cssFile);

        if (!(await isFile(htmlPath)) || !(await isFile(cssPath))) {
            console.warn(`Skipped ${entry.name}: ${htmlFile} or ${cssFile} is missing.`);
            continue;
        }

        projects.push({
            folder: entry.name,
            name: template.name || entry.name,
            id: template.id || entry.name,
            files: { html: htmlFile, css: cssFile },
            json,
            html: await readFile(htmlPath, "utf8"),
            css: await readFile(cssPath, "utf8")
        });
    } catch (error) {
        console.warn(`Skipped ${entry.name}: ${error.message}`);
    }
}

projects.sort((left, right) => left.name.localeCompare(right.name, ["ar", "en"]));

const source = `// Generated by refresh-template-projects.mjs. Do not edit by hand.\n` +
    `window.SAFHAT_TEMPLATE_PROJECTS = ${JSON.stringify(projects, null, 2)};\n`;

await writeFile(outputFile, source, "utf8");

console.log(`Wrote ${projects.length} projects to ${relative(templatesDirectory, outputFile)}.`);
